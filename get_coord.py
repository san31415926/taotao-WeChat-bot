# ============================================================
# 鼠标坐标测量工具
# ============================================================
# 作用：实时显示鼠标在屏幕上的坐标位置
# 左键点击时记录"左键坐标"，右键点击时记录"右键坐标"
#
# 为什么需要这个工具？
# 因为我们要告诉程序"消息在哪里"、"发送按钮在哪里"
# 这些位置是用屏幕坐标（X像素, Y像素）来表示的
# 运行这个工具，把鼠标移到微信窗口的对应位置，就能读出坐标
# 读出来的坐标填到 send_wechat.py 的 forward_to_groups() 函数里
#
# 屏幕坐标系统：
#   (0, 0)     = 屏幕左上角
#   X 轴向右   = 往右 X 值增加
#   Y 轴向下   = 往下 Y 值增加
#   比如 (835, 612) 表示"从左边往右 835 像素，从顶部往下 612 像素"
# ============================================================


# ==================== 第一步：导入要用到的工具库 ====================

import pyautogui
# pyautogui 是 Python 的鼠标键盘自动化库
# 这里我们用它的 position() 方法获取鼠标当前位置
# 用 mouseDown() 方法检测鼠标按键是否被按下

import time
# time.sleep() 让程序暂停一段时间
# 这里用来控制刷新频率——每 0.05 秒读取一次鼠标位置


# ==================== 第二步：打印使用说明 ====================

print("鼠标坐标测量工具")
print("  实时显示鼠标位置，左键点击记录坐标")
print("  Ctrl+C 退出")
print()
# print() 是 Python 的输出函数
# 它会把文字显示在终端/命令行窗口里


# ==================== 第三步：定义状态变量 ====================

left_was_down = False
# 记录"上一轮检查时，左键是否按着"
# 为什么要记录？
#   因为 mouseDown() 只能检测"当前是不是按着"
#   我们想捕捉的是"从没按 → 刚按下的那一刻"
#   所以需要对比"上一轮的状态"和"当前的状态"
#   如果上一轮没按、这一轮按了 = 刚按下 → 记录坐标
#   如果上一轮按着、这一轮也按着 = 一直按着 → 忽略（避免重复记录）

right_was_down = False
# 同上，但针对右键


# ==================== 第四步：主循环 ====================

try:
    # try 块里的代码如果出错，会跳到 except
    # 这里用来捕获用户按 Ctrl+C 时的 KeyboardInterrupt 异常
    # 让程序可以优雅退出，而不是直接崩溃报错

    while True:
        # while True = 无限循环
        # 条件永远是 True，所以永远不会自己停下来
        # 要退出只能靠 Ctrl+C 或者关掉窗口

        # ---- 获取当前鼠标坐标 ----
        x, y = pyautogui.position()
        # position() 返回一个 (X, Y) 元组
        # 元组拆包：把 X 赋值给变量 x，Y 赋值给变量 y
        # 等价于：pos = pyautogui.position(); x = pos[0]; y = pos[1]

        # ---- 检测鼠标按键状态 ----
        left_now = pyautogui.mouseDown(button="left")
        # mouseDown(button="left") 返回布尔值
        # True = 左键当前被按着
        # False = 左键当前没被按着
        right_now = pyautogui.mouseDown(button="right")
        # 同上，针对右键

        # ---- 判断"刚按下"的时刻 ----
        if left_now and not left_was_down:
            # left_now = True（当前按着）
            # left_was_down = False（上一轮没按着）
            # 说明用户在这一瞬间刚按下了左键
            # 这就是"刚按下的时刻"
            print(f"\n>>> 左键坐标: ({x:4d}, {y:4d})")
            # f-string 格式化输出
            # {x:4d} = 把 x 显示为整数，至少占 4 个字符宽度（右对齐）
            # 这样显示出来坐标能对齐，好看

        if right_now and not right_was_down:
            # 同上，针对右键
            print(f"\n>>> 右键坐标: ({x:4d}, {y:4d})")

        # ---- 更新"上一轮状态" ----
        left_was_down = left_now
        right_was_down = right_now
        # 把"当前状态"保存为"上一轮状态"，供下一轮比较

        # ---- 显示实时鼠标坐标（同一行刷新） ----
        print(f"\r鼠标: ({x:4d}, {y:4d})    ", end="")
        # \r = 回车符（carriage return）
        # 作用是把光标移到当前行的开头
        # 然后重新打印，这样下一行内容会覆盖当前行
        # 效果就是：鼠标坐标在同一行不断刷新，不会产生大量输出
        # end="" = 不让 print() 自动换行（默认是 end="\n"）
        # 后面加几个空格是为了覆盖之前可能更长的文字

        time.sleep(0.05)
        # 每 0.05 秒检测一次 = 每秒检测 20 次
        # 太频繁会浪费 CPU，太稀疏会错过点击

except KeyboardInterrupt:
    # KeyboardInterrupt = 键盘中断
    # 用户在终端按了 Ctrl+C 时会触发这个异常
    # 不做特别处理，只是打印一条消息然后退出
    print("\n\n已退出")
